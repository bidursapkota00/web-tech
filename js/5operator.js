// comparision operator
let a = 1;
let b = "1";

const isEqual = a !== b; //  ===  &  ==
console.log(isEqual);

// logical operators
const isTrue = true && false; // ||
console.log(isTrue);
console.log(!isTrue);

// typeof operator
const type = typeof a;
console.log(type);
console.log(typeof type);
